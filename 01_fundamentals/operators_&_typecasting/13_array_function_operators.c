#include <stdio.h>
#include <stdint.h>

/*
===============================================================================
                13_ARRAY_FUNCTION_OPERATORS.C
===============================================================================

TOPIC:
    Array Subscript Operator
    Function Call Operator

OPERATORS:

    []    Array Subscript Operator
    ()    Function Call Operator

===============================================================================

OPERATORS TABLE

+------------+------------------------+-----------------------------+
| OPERATOR   | NAME                   | PURPOSE                     |
+------------+------------------------+-----------------------------+
| []         | Array Subscript        | Access array elements       |
| ()         | Function Call          | Call a function             |
+------------+------------------------+-----------------------------+

===============================================================================

1. ARRAY SUBSCRIPT OPERATOR ([])

GENERAL SYNTAX:

    array[index]

Example:

    numbers[0]

Meaning:

    Access first element.

-------------------------------------------------------------------------------

MEMORY:

    int numbers[5];

    +-----+-----+-----+-----+-----+
    | 10  | 20  | 30  | 40  | 50  |
    +-----+-----+-----+-----+-----+
      [0]   [1]   [2]   [3]   [4]

===============================================================================

IMPORTANT:

ARRAY INDEXING STARTS FROM ZERO.

First Element:

    array[0]

Last Element:

    array[size - 1]

===============================================================================

POINTER EQUIVALENCE

These are equivalent:

    array[2]

    *(array + 2)

Both access exactly the same memory location.

===============================================================================

2. FUNCTION CALL OPERATOR (())

GENERAL SYNTAX:

    function_name(arguments);

Example:

    printf("Hello");

    add(10, 20);

===============================================================================
*/


/*
===============================================================================
FUNCTION EXAMPLES
===============================================================================
*/

int add(int a, int b)
{
    return a + b;
}


void print_line(void)
{
    printf("--------------------------------------\n");
}


/*
===============================================================================
FUNCTION POINTER TARGET
===============================================================================
*/

void led_on_callback(void)
{
    printf("LED ON CALLBACK EXECUTED\n");
}


/*
===============================================================================
MAIN FUNCTION
===============================================================================
*/

int main(void)
{
    /*
    ===========================================================================
    1. ARRAY SUBSCRIPT OPERATOR
    ===========================================================================
    */

    int numbers[5] = {10, 20, 30, 40, 50};

    printf("=========================================\n");
    printf("1. ARRAY SUBSCRIPT OPERATOR ([])\n");
    printf("=========================================\n");

    printf("numbers[0] = %d\n", numbers[0]);
    printf("numbers[1] = %d\n", numbers[1]);
    printf("numbers[2] = %d\n", numbers[2]);
    printf("numbers[3] = %d\n", numbers[3]);
    printf("numbers[4] = %d\n\n", numbers[4]);



    /*
    ===========================================================================
    2. POINTER EQUIVALENCE
    ===========================================================================
    */

    printf("=========================================\n");
    printf("2. POINTER EQUIVALENCE\n");
    printf("=========================================\n");

    printf("numbers[2]     = %d\n",
           numbers[2]);

    printf("*(numbers + 2) = %d\n\n",
           *(numbers + 2));



    /*
    ===========================================================================
    MEMORY VISUALIZATION

    numbers

    Address:

        0x1000

    +-------+-------+-------+-------+-------+
    |  10   |  20   |  30   |  40   |  50   |
    +-------+-------+-------+-------+-------+
       +0      +1      +2      +3      +4

    numbers[2]

        *(numbers + 2)

    ===========================================================================
    */



    /*
    ===========================================================================
    3. ARRAY TRAVERSAL
    ===========================================================================
    */

    printf("=========================================\n");
    printf("3. ARRAY TRAVERSAL\n");
    printf("=========================================\n");

    for (int index = 0; index < 5; index++)
    {
        printf("numbers[%d] = %d\n",
               index,
               numbers[index]);
    }

    printf("\n");



    /*
    ===========================================================================
    4. TWO-DIMENSIONAL ARRAYS
    ===========================================================================
    */

    int matrix[2][3] =
    {
        {1, 2, 3},
        {4, 5, 6}
    };

    printf("=========================================\n");
    printf("4. TWO-DIMENSIONAL ARRAYS\n");
    printf("=========================================\n");

    for (int row = 0; row < 2; row++)
    {
        for (int column = 0; column < 3; column++)
        {
            printf("%d ",
                   matrix[row][column]);
        }

        printf("\n");
    }

    printf("\n");



    /*
    ===========================================================================
    MEMORY VISUALIZATION

    matrix[2][3]

    +-----+-----+-----+
    |  1  |  2  |  3  |
    +-----+-----+-----+
    |  4  |  5  |  6  |
    +-----+-----+-----+

    Access:

        matrix[1][2]

    Result:

        6

    ===========================================================================
    */



    /*
    ===========================================================================
    5. FUNCTION CALL OPERATOR (())
    ===========================================================================
    */

    printf("=========================================\n");
    printf("5. FUNCTION CALL OPERATOR (())\n");
    printf("=========================================\n");

    int result = add(10, 20);

    printf("Result = %d\n\n",
           result);



    /*
    ===========================================================================
    FUNCTION CALL FLOW

        main()

           |

           v

        add(10,20)

           |

           v

        return 30

           |

           v

        main()

    ===========================================================================
    */



    /*
    ===========================================================================
    6. VOID FUNCTION CALL
    ===========================================================================
    */

    printf("=========================================\n");
    printf("6. VOID FUNCTION CALL\n");
    printf("=========================================\n");

    print_line();

    printf("\n");



    /*
    ===========================================================================
    7. FUNCTION POINTERS
    ===========================================================================
    */

    void (*callback)(void);

    callback = led_on_callback;

    printf("=========================================\n");
    printf("7. FUNCTION POINTERS\n");
    printf("=========================================\n");

    callback();

    printf("\n");



    /*
    ===========================================================================
    FUNCTION POINTER VISUALIZATION

    callback

       |

       v

    led_on_callback()

    ===========================================================================
    */



    /*
    ===========================================================================
    EMBEDDED EXAMPLE 1

    ADC SAMPLE BUFFER
    ===========================================================================
    */

    uint16_t adc_samples[4] =
    {
        1024,
        2048,
        3072,
        4095
    };

    printf("=========================================\n");
    printf("EMBEDDED EXAMPLE 1\n");
    printf("=========================================\n");

    for (int i = 0; i < 4; i++)
    {
        printf("ADC[%d] = %u\n",
               i,
               adc_samples[i]);
    }

    printf("\n");



    /*
    ===========================================================================
    EMBEDDED EXAMPLE 2

    LOOKUP TABLE (LUT)

    Sine Wave Table

    Common in:

        PWM
        Motor Control
        DSP
    ===========================================================================
    */

    uint8_t sine_lut[8] =
    {
        128,
        176,
        218,
        245,
        255,
        245,
        218,
        176
    };

    printf("=========================================\n");
    printf("EMBEDDED EXAMPLE 2\n");
    printf("=========================================\n");

    printf("LUT[3] = %u\n\n",
           sine_lut[3]);



    /*
    ===========================================================================
    EMBEDDED EXAMPLE 3

    INTERRUPT CALLBACK

    Common in:

        HAL Libraries
        RTOS
        Drivers
    ===========================================================================
    */

    void (*interrupt_handler)(void);

    interrupt_handler = led_on_callback;

    printf("=========================================\n");
    printf("EMBEDDED EXAMPLE 3\n");
    printf("=========================================\n");

    interrupt_handler();

    printf("\n");



    /*
    ===========================================================================
    COMMON MISTAKES
    ===========================================================================

    WRONG:

        numbers[5]

    Valid Index:

        0 to 4

    Reason:

        Out Of Bounds Access

    ----------------------------------------

    WRONG:

        callback;

    RIGHT:

        callback();

    Reason:

        Function call requires ()

    ----------------------------------------

    WRONG:

        matrix[2][3]

    Maximum Valid:

        matrix[1][2]

    ===========================================================================
    */



    /*
    ===========================================================================
    INTERVIEW QUESTIONS
    ===========================================================================

    Q1:

        Does array indexing start from 0?

        Answer:

            YES

    ----------------------------------------

    Q2:

        Is:

            arr[2]

        same as:

            *(arr + 2)

        Answer:

            YES

    ----------------------------------------

    Q3:

        What operator calls functions?

        Answer:

            ()

    ----------------------------------------

    Q4:

        What is a function pointer?

        Answer:

            A pointer that stores
            a function address.

    ----------------------------------------

    Q5:

        Where are function pointers
        used in Embedded Systems?

        Answer:

            Interrupt handlers
            HAL callbacks
            RTOS hooks
            Driver frameworks

    ===========================================================================
    */

    return 0;
}
